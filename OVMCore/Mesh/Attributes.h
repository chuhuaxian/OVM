#ifndef _OVM_ATTRIBUTES_H_
#define _OVM_ATTRIBUTES_H_

#include <OVM/OVMCore/system/config.h>

namespace OVM
{
	namespace Attributes
	{
		enum AttributeBits
		{
			None = 0,  ///< Clear all attribute bits
			HalfFaceNormal = 1,  ///< Add normals to half face item (half faces)
			HalfFaceRegion = 2,  ///< Add regions to half face item (half faces)
			HedronRegion = 4,  ///< Add regions to hedron item (hedrons)
			Status = 8,  ///< Add status to mesh item (all items)
		};
	}
}



#endif // !_OVM_ATTRIBUTES_H_
