#define _OVM_TOOLS_SMOOTHER_H_ 

namespace OVM
{
	namespace Util
	{
		/** this class is to provide the smoothing operation for meshes
		*/
		template < class MeshT >
		class SmootherT
		{
		public:
			SmootherT(MeshT & _mesh) : mesh_(_mesh)
			{
			}
			virtual ~ SmootherT()
			{
			}
		public:
			void smooth(unsigned int _n);
		private:
			MeshT & mesh_;
		};
	}
}
